package com.batch.model.TVLFile;

public class TVLPlateDetails {
    private String plateNumber;
    private String plateState;
    private String plateEffectiveFrom;
    private String plateCountry;
    public String getPlateNumber() {
        return plateNumber;
    }
    public void setPlateNumber(String plateNumber) {
        this.plateNumber = plateNumber;
    }
    public String getPlateState() {
        return plateState;
    }
    public void setPlateState(String plateState) {
        this.plateState = plateState;
    }
    public String getPlateEffectiveFrom() {
        return plateEffectiveFrom;
    }
    public void setPlateEffectiveFrom(String plateEffectiveFrom) {
        this.plateEffectiveFrom = plateEffectiveFrom;
    }
    public String getPlateCountry() {
        return plateCountry;
    }
    public void setPlateCountry(String plateCountry) {
        this.plateCountry = plateCountry;
    }
}

